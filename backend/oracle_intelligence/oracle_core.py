    async def analyze_and_predict(
        self,
        code: str,
        context: Optional[str] = None,
        file_path: Optional[str] = None,
        user_intent: Optional[str] = None
    ) -> List[OracleInsight]:
        """
        Analyze code and generate predictive insights.

        This is the main entry point for the Oracle's analysis.
        """
        insights = []

        # Generate insight ID base
        analysis_id = f"ANALYZE-{uuid.uuid4().hex[:8]}"

        try:
            # 1. Pattern Recognition
            pattern_insights = await self._recognize_patterns(code, file_path)
            insights.extend(pattern_insights)

            # 2. Forward Simulation (7 steps ahead)
            simulation = await self._simulate_forward(code, context, 7)
            if simulation.risk_points:
                for risk in simulation.risk_points:
                    insight = OracleInsight(
                        insight_id=f"RISK-{uuid.uuid4().hex[:8]}",
                        insight_type=InsightType.FAILURE_PREDICTION,
                        title=risk.get("title", "Predicted Issue"),
                        description=risk.get("description", ""),
                        confidence=self._score_to_level(risk.get("probability", 0.5)),
                        confidence_score=risk.get("probability", 0.5),
                        prediction_horizon=risk.get("step", 1),
                        prevention_actions=risk.get("prevention", []),
                        affected_files=[file_path] if file_path else []
                    )
                    insights.append(insight)

            # 3. Cascading Failure Analysis
            cascading = await self._analyze_cascading_risks(code, file_path)
            insights.extend(cascading)

            # 4. User Intent Prediction
            if user_intent:
                intent_insights = await self._predict_user_needs(user_intent, code)
                insights.extend(intent_insights)

            # 5. Store insights with Genesis keys
            for insight in insights:
                self._insights[insight.insight_id] = insight
                await self._create_insight_genesis_key(insight)

            self.metrics["insights_generated"] += len(insights)
            self.metrics["predictions_made"] += len([i for i in insights if i.insight_type == InsightType.FAILURE_PREDICTION])

        except Exception as e:
            logger.error(f"[ORACLE] Analysis failed: {e}")

        return insights

    async def think_ahead(
        self,
        current_action: str,
        context: str,
        depth: int = 7
    ) -> ForwardSimulation:
        """
        Think N steps ahead like a chess grandmaster.

        Simulates potential outcomes of an action and identifies
        risks, opportunities, and optimal paths.
        """
        simulation = ForwardSimulation(
            simulation_id=f"SIM-{uuid.uuid4().hex[:12]}",
            current_state=current_action,
            depth=min(depth, 7)
        )

        # Build decision tree
        current_states = [{"state": current_action, "context": context, "step": 0}]

        for step in range(1, depth + 1):
            next_states = []

            for state in current_states:
                # Predict next possible states
                outcomes = await self._predict_next_states(state, step)

                for outcome in outcomes:
                    simulation.steps.append({
                        "step": step,
                        "from_state": state["state"][:100],
                        "to_state": outcome["state"][:100],
                        "probability": outcome["probability"],
                        "is_risky": outcome.get("is_risky", False)
                    })

                    # Track risks
                    if outcome.get("is_risky"):
                        simulation.risk_points.append({
                            "step": step,
                            "title": outcome.get("risk_title", "Potential Issue"),
                            "description": outcome.get("risk_description", ""),
                            "probability": outcome["probability"],
                            "prevention": outcome.get("prevention", [])
                        })

                    # Track cascading risks
                    if outcome.get("cascading_risk"):
                        simulation.cascading_risks.append(outcome["cascading_risk"])

                    next_states.append({
                        "state": outcome["state"],
                        "context": state["context"],
                        "step": step
                    })

            current_states = next_states[:5]  # Limit branching

        # Determine recommended path
        simulation.recommended_path = self._find_optimal_path(simulation)
        simulation.confidence = self._calculate_simulation_confidence(simulation)

        # Cache simulation
        self._simulation_cache[simulation.simulation_id] = simulation

        return simulation

    async def _recognize_patterns(
        self,
        code: str,
        file_path: Optional[str]
    ) -> List[OracleInsight]:
        """Recognize patterns in code and match against known patterns."""
        insights = []

        # Extract code patterns
        patterns = self._extract_patterns(code)

        for pattern_type, pattern_value in patterns.items():
            pattern_hash = hashlib.md5(f"{pattern_type}:{pattern_value}".encode()).hexdigest()[:12]

            # Check against known failure patterns
            if pattern_hash in self._failure_patterns:
                failure_info = self._failure_patterns[pattern_hash]
                insights.append(OracleInsight(
                    insight_id=f"PAT-{uuid.uuid4().hex[:8]}",
                    insight_type=InsightType.PATTERN,
                    title=f"Known failure pattern: {pattern_type}",
                    description=failure_info.get("description", "This pattern has caused issues before"),
                    confidence=ConfidenceLevel.HIGH,
                    confidence_score=failure_info.get("confidence", 0.8),
                    prevention_actions=failure_info.get("fixes", []),
                    source_patterns=[pattern_hash],
                    affected_files=[file_path] if file_path else []
                ))

            # Track pattern for learning
            if pattern_hash not in self._code_patterns:
                self._code_patterns[pattern_hash] = []
            self._code_patterns[pattern_hash].append({
                "file": file_path,
                "timestamp": datetime.utcnow().isoformat()
            })

        self.metrics["patterns_discovered"] = len(self._code_patterns)

        return insights

    def _extract_patterns(self, code: str) -> Dict[str, str]:
        """Extract recognizable patterns from code."""
        import re
        patterns = {}

        # Error handling patterns
        if "except:" in code and "except Exception" not in code:
            patterns["bare_except"] = "bare except clause"

        if "try:" in code and "finally:" not in code:
            patterns["try_no_finally"] = "try without finally for cleanup"

        # Null safety patterns
        null_checks = len(re.findall(r'if\s+\w+\s+is\s+(not\s+)?None', code))
        if null_checks == 0 and "." in code:
            patterns["no_null_checks"] = "potential null dereference"

        # Import patterns
        if "import *" in code:
            patterns["star_import"] = "wildcard import"

        # Security patterns
        if "eval(" in code or "exec(" in code:
            patterns["dangerous_eval"] = "use of eval/exec"

        if "password" in code.lower() and "=" in code:
            patterns["hardcoded_secret"] = "potential hardcoded password"

        # Performance patterns
        if "+=" in code and "for" in code and "str" in code.lower():
            patterns["string_concat_loop"] = "string concatenation in loop"

        # Complexity patterns
        indent_levels = max((len(line) - len(line.lstrip())) // 4 for line in code.split("\n") if line.strip())
        if indent_levels > 5:
            patterns["deep_nesting"] = f"deep nesting ({indent_levels} levels)"

        return patterns

    async def _simulate_forward(
        self,
        code: str,
        context: Optional[str],
        depth: int
    ) -> ForwardSimulation:
        """Run forward simulation on code changes."""
        return await self.think_ahead(
            current_action=f"Code change: {code[:200]}...",
            context=context or "Code modification",
            depth=depth
        )

