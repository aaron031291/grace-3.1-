        self._learning_queue: List[Dict[str, Any]] = []
        self._prediction_accuracy: List[Tuple[str, bool]] = []  # For calibration

        # Metrics
        self.metrics = {
            "insights_generated": 0,
            "research_entries": 0,
            "predictions_made": 0,
            "predictions_correct": 0,
            "cascading_failures_prevented": 0,
            "patterns_discovered": 0
        }

