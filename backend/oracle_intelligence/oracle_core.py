    def _find_optimal_path(self, simulation: ForwardSimulation) -> List[str]:
        """Find the optimal path through the simulation."""
        path = []

        # Filter to low-risk steps
        safe_steps = [s for s in simulation.steps if not s.get("is_risky", False)]

        # Build path through safe steps
        current_step = 0
        for step in sorted(safe_steps, key=lambda x: (x["step"], -x["probability"])):
            if step["step"] > current_step:
                path.append(step["to_state"])
                current_step = step["step"]

            if len(path) >= simulation.depth:
                break

        return path

    def _calculate_simulation_confidence(self, simulation: ForwardSimulation) -> float:
        """Calculate overall confidence in the simulation."""
        if not simulation.steps:
            return 0.5

        # Average probability of steps
        avg_prob = sum(s["probability"] for s in simulation.steps) / len(simulation.steps)

        # Reduce confidence with more risks
        risk_penalty = len(simulation.risk_points) * 0.05

        return max(0.1, min(0.95, avg_prob - risk_penalty))

    def _score_to_level(self, score: float) -> ConfidenceLevel:
        """Convert score to confidence level."""
        if score >= 0.95:
            return ConfidenceLevel.CERTAIN
        elif score >= 0.80:
            return ConfidenceLevel.HIGH
        elif score >= 0.60:
            return ConfidenceLevel.MEDIUM
        elif score >= 0.40:
            return ConfidenceLevel.LOW
        else:
            return ConfidenceLevel.SPECULATIVE

    async def _create_insight_genesis_key(self, insight: OracleInsight):
        """Create Genesis key for insight tracking."""
        if not self._genesis_service:
            return

        try:
            from models.genesis_key_models import GenesisKeyType
            key = self._genesis_service.create_key(
                key_type=GenesisKeyType.ORACLE_INSIGHT,
                what_description=f"Oracle Insight: {insight.title[:50]}",
                who_actor="Oracle",
                where_location=",".join(insight.affected_files[:3]) if insight.affected_files else "system",
                why_reason=f"Predictive analysis - {insight.insight_type.value}",
                how_method="Pattern recognition and forward simulation",
                context_data={
                    "insight_id": insight.insight_id,
                    "type": insight.insight_type.value,
                    "confidence": insight.confidence_score,
                    "horizon": insight.prediction_horizon
                },
                session=self.session
            )
            insight.genesis_key_id = key.key_id
        except Exception as e:
            logger.warning(f"[ORACLE] Genesis key creation failed: {e}")

    async def store_research(
        self,
        topic: str,
        findings: str,
        code_examples: Optional[List[str]] = None,
        source_files: Optional[List[str]] = None
    ) -> ResearchEntry:
        """Store research findings in the AI research knowledge base."""
        entry = ResearchEntry(
            research_id=f"RESEARCH-{uuid.uuid4().hex[:12]}",
            topic=topic,
            findings=findings,
            code_examples=code_examples or [],
            source_files=source_files or [],
            confidence=0.7,
            created_at=datetime.utcnow()
        )

        # Create Genesis key
        if self._genesis_service:
            try:
                from models.genesis_key_models import GenesisKeyType
                key = self._genesis_service.create_key(
                    key_type=GenesisKeyType.RESEARCH_STORED,
                    what_description=f"AI Research: {topic[:50]}",
                    who_actor="Oracle",
                    where_location="ai_research",
                    why_reason="Knowledge base expansion",
                    how_method="Research and pattern analysis",
                    context_data={
                        "research_id": entry.research_id,
                        "topic": topic
                    },
                    session=self.session
                )
                entry.genesis_key_id = key.key_id
            except Exception as e:
                logger.warning(f"[ORACLE] Research genesis key failed: {e}")

        # Store in memory
        self._research[entry.research_id] = entry

        # Store to file system
        await self._persist_research(entry)

        # Store in vector DB for semantic search
        if self._vector_db:
            await self._index_research(entry)

        self.metrics["research_entries"] += 1

        logger.info(f"[ORACLE] Research stored: {topic}")

        return entry

    async def _persist_research(self, entry: ResearchEntry):
        """Persist research to file system."""
        try:
            research_file = self.research_path / f"{entry.research_id}.json"
            with open(research_file, "w") as f:
                json.dump(entry.to_dict(), f, indent=2)
        except Exception as e:
            logger.warning(f"[ORACLE] Research persistence failed: {e}")

    async def _index_research(self, entry: ResearchEntry):
        """Index research in vector DB for semantic search."""
        if not self._vector_db:
            return

        try:
            from embedding.embedder import get_embedder
            embedder = get_embedder()

            text = f"{entry.topic} {entry.findings}"
            embedding = await embedder.embed(text)

            await self._vector_db.upsert(
                collection_name="oracle_research",
                points=[{
                    "id": entry.research_id,
                    "vector": embedding,
                    "payload": entry.to_dict()
                }]
            )
        except Exception as e:
            logger.warning(f"[ORACLE] Research indexing failed: {e}")

    async def search_research(
        self,
        query: str,
        limit: int = 5
    ) -> List[ResearchEntry]:
        """Search research by semantic similarity."""
        if not self._vector_db:
            # Fallback to keyword search
            results = []
            query_lower = query.lower()
            for entry in self._research.values():
                if query_lower in entry.topic.lower() or query_lower in entry.findings.lower():
                    results.append(entry)
            return results[:limit]

        try:
            from embedding.embedder import get_embedder
            embedder = get_embedder()

            embedding = await embedder.embed(query)
            results = await self._vector_db.search(
                collection_name="oracle_research",
                query_vector=embedding,
                limit=limit
            )

            entries = []
            for result in results:
                if result.id in self._research:
                    self._research[result.id].usage_count += 1
                    entries.append(self._research[result.id])

            return entries

        except Exception as e:
            logger.warning(f"[ORACLE] Research search failed: {e}")
            return []

    async def learn_from_outcome(
        self,
        insight_id: str,
        was_accurate: bool,
        actual_outcome: Optional[str] = None
    ):
        """Learn from the accuracy of a prediction."""
        if insight_id not in self._insights:
            return

        insight = self._insights[insight_id]
        insight.was_accurate = was_accurate

        # Update prediction accuracy tracking
        self._prediction_accuracy.append((insight_id, was_accurate))
        if len(self._prediction_accuracy) > 1000:
            self._prediction_accuracy = self._prediction_accuracy[-1000:]

        if was_accurate:
            self.metrics["predictions_correct"] += 1

            # Reinforce the patterns that led to this insight
            for pattern in insight.source_patterns:
                if pattern in self._failure_patterns:
                    self._failure_patterns[pattern]["confidence"] = min(
                        0.99,
                        self._failure_patterns[pattern].get("confidence", 0.7) + 0.05
                    )

        else:
            # Reduce confidence in patterns
            for pattern in insight.source_patterns:
                if pattern in self._failure_patterns:
                    self._failure_patterns[pattern]["confidence"] = max(
                        0.1,
                        self._failure_patterns[pattern].get("confidence", 0.7) - 0.1
                    )

        # Store learning
        await self._queue_learning({
            "type": "prediction_outcome",
            "insight_id": insight_id,
            "was_accurate": was_accurate,
            "actual_outcome": actual_outcome,
            "timestamp": datetime.utcnow().isoformat()
        })

    async def _queue_learning(self, learning: Dict[str, Any]):
        """Queue learning for processing."""
        self._learning_queue.append(learning)

        # Process in batches
        if len(self._learning_queue) >= 10:
            await self._process_learning_queue()

    async def _process_learning_queue(self):
        """Process queued learning items."""
        if not self._learning_queue:
            return

        # Extract patterns from learning
        for item in self._learning_queue:
            if item["type"] == "prediction_outcome":
                # Store as research
                await self.store_research(
                    topic=f"Prediction Accuracy: {item['insight_id']}",
                    findings=f"Prediction was {'accurate' if item['was_accurate'] else 'inaccurate'}. Actual: {item.get('actual_outcome', 'unknown')}"
                )

        self._learning_queue = []

    def get_active_insights(self) -> List[Dict[str, Any]]:
        """Get all active insights."""
        return [
            i.to_dict() for i in self._insights.values()
            if i.is_active and (not i.expires_at or i.expires_at > datetime.utcnow())
        ]

    def get_prediction_accuracy(self) -> float:
        """Get overall prediction accuracy."""
        if not self._prediction_accuracy:
            return 0.0

        correct = sum(1 for _, accurate in self._prediction_accuracy if accurate)
        return correct / len(self._prediction_accuracy)

    def get_metrics(self) -> Dict[str, Any]:
        """Get Oracle metrics."""
        return {
            **self.metrics,
            "prediction_accuracy": self.get_prediction_accuracy(),
            "active_insights": len([i for i in self._insights.values() if i.is_active]),
            "patterns_tracked": len(self._code_patterns),
            "failure_patterns": len(self._failure_patterns),
            "success_patterns": len(self._success_patterns),
            "pending_learnings": len(self._learning_queue)
        }

    def get_research_summary(self) -> Dict[str, Any]:
        """Get summary of stored research."""
        return {
            "total_entries": len(self._research),
            "recent_topics": [
                r.topic for r in sorted(
                    self._research.values(),
                    key=lambda x: x.created_at,
                    reverse=True
                )[:10]
            ],
            "most_used": [
                {"topic": r.topic, "usage": r.usage_count}
                for r in sorted(
                    self._research.values(),
                    key=lambda x: x.usage_count,
                    reverse=True
                )[:5]
            ]
        }
