logger = logging.getLogger(__name__)


class InsightType(str, Enum):
    """Types of insights the Oracle can generate."""
    PATTERN = "pattern"               # Recurring code patterns
    FAILURE_PREDICTION = "failure"    # Predicted failures
    OPTIMIZATION = "optimization"     # Performance improvements
    SECURITY = "security"             # Security concerns
    ARCHITECTURE = "architecture"     # Design patterns
    DEPENDENCY = "dependency"         # Dependency issues
    CASCADING_RISK = "cascading"      # Cascading failure risk
    USER_INTENT = "user_intent"       # Predicted user needs


class ConfidenceLevel(str, Enum):
    """Confidence levels for predictions."""
    CERTAIN = "certain"       # 95%+ confidence
    HIGH = "high"             # 80-95%
    MEDIUM = "medium"         # 60-80%
    LOW = "low"               # 40-60%
    SPECULATIVE = "speculative"  # <40%


@dataclass
class OracleInsight:
    """An insight generated by the Oracle."""
    insight_id: str
    insight_type: InsightType
    title: str
    description: str
    confidence: ConfidenceLevel
    confidence_score: float
    affected_files: List[str] = field(default_factory=list)
    affected_functions: List[str] = field(default_factory=list)
    prediction_horizon: int = 1  # How many steps ahead (1-7)
    prevention_actions: List[str] = field(default_factory=list)
    genesis_key_id: Optional[str] = None
    source_patterns: List[str] = field(default_factory=list)
    created_at: datetime = field(default_factory=datetime.utcnow)
    expires_at: Optional[datetime] = None
    is_active: bool = True
    was_accurate: Optional[bool] = None  # For learning

    def to_dict(self) -> Dict[str, Any]:
        return {
            "insight_id": self.insight_id,
            "type": self.insight_type.value,
            "title": self.title,
            "description": self.description,
            "confidence": self.confidence.value,
            "confidence_score": self.confidence_score,
            "affected_files": self.affected_files,
            "affected_functions": self.affected_functions,
            "prediction_horizon": self.prediction_horizon,
            "prevention_actions": self.prevention_actions,
            "genesis_key_id": self.genesis_key_id,
            "created_at": self.created_at.isoformat(),
            "is_active": self.is_active
        }


